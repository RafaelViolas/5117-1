# perimetro l*2 + c*2
largura = int(input('Indique a largura do campo em metros: '))
comprimento = int(input('Indique o comprimento do campo em metros: '))
area = largura * comprimento
perimetro = largura * 2 + comprimento * 2
print(f'\nA Area do campo é {area} metros quadrados')
print(f'O perimetro do campo é {perimetro} metros')
